#ifndef PROJECT_CARD_GAME_PLAYER_H
#define PROJECT_CARD_GAME_PLAYER_H

#include <vector>
#include <iostream>
#include "Card.h"

using namespace std;

class Player {
    string _name;
    int _prestige;
    vector<Card> _deck;

public :
    /*
    *************************************************************************************************
    *                                                                                               *
    *                                                                                               *
    * Description :                                                                                 *
    * ===========                                                                                   *
    * Empty constructor with default values                                                         *
    *                                                                                               *
    *                                                                                               *
    *************************************************************************************************
    */

    Player();

    /*
    *************************************************************************************************
    *                                                                                               *
    *                                                                                               *
    * Description :                                                                                 *
    * ===========                                                                                   *
    * Player constructor                                                                            *
    *                                                                                               *
    *                                                                                               *
    *************************************************************************************************
    */

    Player(string, int, vector<Card>);


    /*
    *************************************************************************************************
    *                                                                                               *
    *                                                                                               *
    * Description :                                                                                 *
    * ===========                                                                                   *
    * get the name of targeted player                                                               *
    *                                                                                               *
    *                                                                                               *
    *************************************************************************************************
    */

    string GetName() const;

    /*
    *************************************************************************************************
    *                                                                                               *
    *                                                                                               *
    * Description :                                                                                 *
    * ===========                                                                                   *
    * get the number prestige points of targeted player                                             *
    *                                                                                               *
    *                                                                                               *
    *************************************************************************************************
    */

    int GetPrestige() const;

    /*
    *************************************************************************************************
    *                                                                                               *
    *                                                                                               *
    * Description :                                                                                 *
    * ===========                                                                                   *
    * Displaying targeted player information                                                        *
    *                                                                                               *
    *                                                                                               *
    *************************************************************************************************
    */

    void display();

    /*
    *************************************************************************************************
    *                                                                                               *
    *                                                                                               *
    * Description :                                                                                 *
    * ===========                                                                                   *
    * Initialising the targeted player's pile of cards                                              *
    *                                                                                               *
    *                                                                                               *
    *************************************************************************************************
    */

    void init(vector<Card>&);

    /*
    *************************************************************************************************
    *                                                                                               *
    *                                                                                               *
    * Description :                                                                                 *
    * ===========                                                                                   *
    * returns true if there is enough cards in the deck                                             *
    *                                                                                               *
    *                                                                                               *
    *************************************************************************************************
    */

    bool enoughCards();

    /*
    *************************************************************************************************
    *                                                                                               *
    *                                                                                               *
    * Description :                                                                                 *
    * ===========                                                                                   *
    * Prints the next card if there is one if not throw an exception                                *
    *                                                                                               *
    *                                                                                               *
    *************************************************************************************************
    */

    void nextCard(Card &);

    void playsACard(Player &player);
};
#endif //PROJECT_CARD_GAME_PLAYER_H

